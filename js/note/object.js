// Object is created with: 
const testObject = {
    attrbt1: "true",
    attrbt2: "blue",
    attrbt3: "metal",
};

// Object information can be accessed with:
// 1. Dot notation: 
testObject.attrbt1 
// 2. Square Bracket:
testObject['attrbt2']

// New attributes can be added as follow:
testObject.attrbt4 = "wing"
// Or
testObject["attrbt5"] = "spoiler"
